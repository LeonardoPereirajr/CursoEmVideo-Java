    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
    {//GEN-HEADEREND:event_jButton1ActionPerformed
        
        int num = Integer.parseInt(txt_Num.getValue().toString());
        int r = num % 2;
        resul_div.setText(Integer.toString(r));
        panCalc.setVisible(true);
        
        double c = Math.pow(num,3);
        result_cubo.setText(Double.toString(c));
        
        double q = Math.sqrt(num);
        result_quadrado.setText(String.format("%.2f",q));
        
        double rc = Math.cbrt(num);
        result_raizcubica.setText(String.format("%.2f",rc));
        
        int abs = Math.abs(num);
        result_abs.setText(Integer.toString(abs));
               
    }//GEN-LAST:event_jButton1ActionPerformed

