public class Calculadora {

    public int soma(int operador, int operando) {
        return operador + operando;
    }

    public int multiplica(int operador, int operando) {
        return operador * operando;

    }
    public int divide(int operador, int operando) { 
        return operador/operando;
    }
    public int subtrai(int operador, int operando){
        return operador-operando;
    }
    
    
}
