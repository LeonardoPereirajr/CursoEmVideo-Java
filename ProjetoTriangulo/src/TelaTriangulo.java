    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnVerificarActionPerformed
    {//GEN-HEADEREND:event_btnVerificarActionPerformed
        panResposta.setVisible(true);
        int a = sliA.getValue();
        int b = sliB.getValue();
        int c = sliC.getValue();
        if ((a < (b + c) && (b < (a + c)) && (c < (a + b))))
        {
            lblStatus.setText("Formam um triangulo");
            if ((a == b) && (b == c))
            {
                lblTipo.setText("Equilatero");
            } else if (a != b && b != c && a != c)
            {
                lblTipo.setText("Escaleno");
            } else
            {
                lblTipo.setText("IsÃ³sceles");
            }
        } else
        {
            lblStatus.setText("NÃ£o formam um triagulo");
            lblTipo.setText("---");
        }
    }//GEN-LAST:event_btnVerificarActionPerformed

